#include <bits/stdc++.h>
using namespace std;

int main()
{
    int apples =5;
    cout<<apples<<endl;
    cout<<"Size of int:"<<endl<<sizeof(apples)<<endl;

    char sign = '&';
    cout<<sign<<endl;
    cout<<"Size of char:"<<endl;

    bool flag = true;
    cout<<flag<<endl;
    cout<<"size of bool:"<<sizeof(flag)<<endl;

    float score = 3.14;
    cout<<score<<endl;
    cout<<"size of float:"<<sizeof(score)<<endl;
     
    return 0;
}